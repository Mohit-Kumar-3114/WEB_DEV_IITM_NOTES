// LOOPS IN JS

// for loop
// for(initialization; condition; updation){
//     code to be executed
// }
for(let a=0; a<10; a++){
    console.log(a)
}




// while loop
// while(condition){
//     code to be executed
//     updation
// }
let b=0;
while(b<4){
    console.log(b);
    b++;
}




// do while loop
// do{
//     code to be executed
//     updation
// } while(condition)
let c=0;
do{
    console.log(c)
    c++;
} while(c>7)

console.log(c) // 1
