// NUMBERS IN JS

let a = "10"
let aNum = parseInt(a) // parseInt convert a to integer
console.log(a) // 10 as string 
console.log(aNum) // 10 as number

let b = 10.4321
let bNum = parseInt(b)
console.log(b) // 10.4321
console.log(bNum) // 10

let c = "10.4321"
let cNum = parseInt(c)
console.log(c) // 10.4321 as string
console.log(cNum) // 10

let d = "45ohjv23"
let dNum = parseInt(d)
console.log(d) // 45ohjv23 as string
console.log(dNum) // 45

let e = "ohjv23"
let eNum = parseInt(e)
console.log(e) // ohjv23 as string
console.log(eNum) // NaN




let f = "45"
let fNum = parseFloat(f) // parseFloat convert f to float
console.log(f) // 45 as string
console.log(fNum) // 45

let g = "45.231"
let gNum = parseFloat(g)
console.log(g) // 45.231 as string
console.log(gNum) // 45.231