// DATES IN JS

let date = new Date() // creates a new date object

console.log(date) // full date and time

console.log(date.getDate()) // todays date

console.log(date.getDay()) // index of week days

console.log(date.getMonth()) // index of months

console.log(date.getFullYear()) // current year

console.log(date.getYear()) // current year - 1900

console.log(date.getHours()) // current hour in 24 hour format

console.log(date.getMinutes()) // current minute

console.log(date.getSeconds()) // current second

console.log(date.getTime()) // current time in milliseconds since 1970

