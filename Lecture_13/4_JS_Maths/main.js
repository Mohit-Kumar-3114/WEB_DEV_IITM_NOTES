// MATHS IN JS

let a = 10.123
console.log(a) // 10.123
console.log(Math.round(a)) // 10 rounds to the nearest integer
console.log(Math.ceil(a)) // 11 rounds to the upper integer
console.log(Math.floor(a)) // 10 rounds to the lower integer




console.log(Math.random()) // random value
console.log(Math.max(10,20,30,4,5)) // 30 
console.log(Math.min(2,-1,5,-10,7)) // -10
console.log(Math.pow(2,5)) // 2^5 = 32
console.log(Math.sqrt(25)) // 5