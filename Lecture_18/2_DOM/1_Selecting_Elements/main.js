
// 1. Selecting Elements by TagName
// return html collection of all the divs
let allDivs = document.getElementsByTagName("div")
console.log(allDivs)



// 2. Selecting Elements by Class
// return html collection of all the elements with class movie
let allMovies = document.getElementsByClassName("movie")
console.log(allMovies)




