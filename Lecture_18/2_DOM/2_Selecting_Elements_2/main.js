
// Query Selector
// return the first element with the given selector
let singleDiv = document.querySelector("div")
console.log(singleDiv)

// return all the elements with the given selector
let allDivs = document.querySelectorAll("div")
console.log(allDivs)




// return all the elements with the given class
let allMovies = document.querySelectorAll(".movie")
console.log(allMovies)





// return the element with the given id
let singleMovie = document.querySelector("#bazigar")
console.log(singleMovie)